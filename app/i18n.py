def normalize_locale(locale):
    if locale not in TRANSLATIONS:
        return "ar"
    return locale


def get_locale(default_locale="ar"):
    query_locale = request.args.get("lang")
    cookie_locale = request.cookies.get("locale")
    return normalize_locale(query_locale or cookie_locale or default_locale)


def get_texts(locale):
    return TRANSLATIONS[normalize_locale(locale)]
